// Simple i18n (es/en) with localStorage persistence and event-based updates.
import { useEffect, useState } from "react";

export type Lang = "es" | "en";
const KEY = "insulina:lang";

export function getLang(): Lang {
  if (typeof window === "undefined") return "es";
  const v = window.localStorage.getItem(KEY);
  if (v === "es" || v === "en") return v;
  // default: Spanish
  return "es";
}

export function setLang(l: Lang) {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(KEY, l);
  window.dispatchEvent(new Event("insulina:lang"));
  window.dispatchEvent(new Event("insulina:update"));
}

export function useLang(): Lang {
  const [lang, setL] = useState<Lang>("es");
  useEffect(() => {
    setL(getLang());
    const h = () => setL(getLang());
    window.addEventListener("insulina:lang", h);
    return () => window.removeEventListener("insulina:lang", h);
  }, []);
  return lang;
}

