export function t(key: string, params?: Record<string, string | number>, lang?: Lang): string {
  const l = lang ?? getLang();
  let s = DICTS[l][key] ?? DICTS.en[key] ?? key;
  if (params) {
    for (const k in params) s = s.replaceAll(`{${k}}`, String(params[k]));
  }
  return s;
}

export function locale(lang?: Lang): string {
  return (lang ?? getLang()) === "es" ? "es-ES" : "en-US";
}
